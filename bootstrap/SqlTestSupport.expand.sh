#!/usr/bin/env bash
set -euo pipefail

output_dir="${1:-dist}"
mkdir -p "$output_dir"

decode_base64() {
  if base64 --decode >/dev/null 2>&1 </dev/null; then
    base64 --decode
  elif base64 -d >/dev/null 2>&1 </dev/null; then
    base64 -d
  else
    base64 -D
  fi
}

write_file() {
  local relative_path="$1"
  local destination="$output_dir/$relative_path"
  mkdir -p "$(dirname "$destination")"
  decode_base64 > "$destination"
  printf 'Wrote %s\n' "$destination"
}

